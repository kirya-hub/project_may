class Command(BaseCommand):
    help = 'Загружает меню Coffee Cava в БД'

    def handle(self, *args, **options):
        cafe, created = Cafe.objects.get_or_create(
            name=CAFE_NAME,
            defaults=CAFE_DEFAULTS,
        )
        action = 'Создано' if created else 'Найдено'
        self.stdout.write(f'{action} кафе: {cafe.name} (pk={cafe.pk})')

        total_created = 0

        for cat_title, items in MENU.items():
            category, cat_created = MenuCategory.objects.get_or_create(
                cafe=cafe,
                title=cat_title,
            )
            if cat_created:
                self.stdout.write(f'  + Категория: {cat_title}')
            else:
                self.stdout.write(f'  ✓ Категория: {cat_title} (уже есть)')

            for item_name, price in items:
                _, item_created = MenuItem.objects.get_or_create(
                    category=category,
                    name=item_name,
                    defaults={'price': Decimal(str(price))},
                )
                if item_created:
                    total_created += 1

        self.stdout.write(
            self.style.SUCCESS(f'\nГотово! Добавлено {total_created} позиций меню.')
        )
